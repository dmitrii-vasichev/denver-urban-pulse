-- Migration 004: Reference tables
-- Canonical neighborhood name mapping for cross-dataset normalization.

CREATE TABLE IF NOT EXISTS ref_neighborhoods (
    id SERIAL PRIMARY KEY,
    canonical_name TEXT NOT NULL UNIQUE,
    nbhd_id SMALLINT,
    -- Alternate names found in different datasets
    crime_name TEXT,          -- NEIGHBORHOOD_ID in crime dataset
    crash_name TEXT,          -- neighborhood_id in crash dataset
    name_311 TEXT,            -- Neighborhood in 311 dataset
    typology TEXT
);

